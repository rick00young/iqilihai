			}else{
				$slide.animate({top: '-=' + height},800);
				page++;
			}
		}
	});

	$('#downBird').click(function(){
		if( !$slide.is(':animated') ){
			if( page == 1 ){
				$slide.animate({top: '-=' + height*(pageCount - 1)},800);
				page = pageCount;
			}else{
				$slide.animate({top: '+=' + height},800);
				page--;
			}
		}
	});


